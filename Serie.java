/**
 * Programa: Serie númeria del 1 al 10
 * 
 * Descripción:
 * Este programa hace uso de un bucle for para generar
 * numeros desde el 1 hasta un número 10.
 * 
 * @author TonyGc-76c
 * 
 */
public class Serie {
	public static void main(String[] args) {
		int i;
		int n = 10;
		for (i = 0; i <= n; i = i + 1) {
			System.out.println(i);
		}
	}
}
